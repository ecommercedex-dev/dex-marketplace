let currentTipIndex = 0;
const tipText = document.getElementById("tipText");
const tipIcon = document.getElementById("tipIcon");
const tipCounter = document.getElementById("tipCounter");
const banner = document.getElementById("safetyTipsBanner");
const scamRibbon = document.getElementById("scamRibbon");

function showNextTip() {
  currentTipIndex = (currentTipIndex + 1) % safetyTips.length;
  const tip = safetyTips[currentTipIndex];

  // Red ribbon + border for real scam stories
  if (tip.story) {
    scamRibbon.style.opacity = "1";
    banner.style.border = "1.8px solid #ef4444";
    banner.style.boxShadow = "0 6px 28px rgba(239, 68, 68, 0.4)";
  } else {
    scamRibbon.style.opacity = "0";
    banner.style.border = "1.4px solid #f0f0f0";
    banner.style.boxShadow = "0 4px 15px rgba(0,0,0,0.1)";
  }

  tipText.style.opacity = "0";
  tipIcon.style.opacity = "0";

  setTimeout(() => {
    tipText.textContent = tip.text;
    tipIcon.innerHTML = `<i class="fas ${tip.icon} fa-lg"></i>`;
    tipCounter.textContent = currentTipIndex + 1;
    banner.style.background = tip.color;
  }, 500);

  setTimeout(() => {
    tipText.style.opacity = "1";
    tipIcon.style.opacity = "1";
  }, 600);
}

// Initialise first tip
const firstTip = safetyTips[0];
tipText.textContent = firstTip.text;
tipIcon.innerHTML = `<i class="fas ${firstTip.icon} fa-lg"></i>`;
tipCounter.textContent = "1";
banner.style.background = firstTip.color;

// Start rotation (9 seconds — perfect for reading scam stories)
setInterval(showNextTip, 9000);
